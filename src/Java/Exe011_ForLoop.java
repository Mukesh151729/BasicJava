package Java;

public class Exe011_ForLoop {
    public static void main(String[] args) {
        for(int i=0;i<=50;i++)
        {
            if(i%2==0)
            {
                System.out.println(i +"is evene number");
            }
            else {
                System.out.println(i + "is odd number");
            }
        }
    }
}
